    public void setData(Product product) throws SQLException {
        setCategoryComboBoxData(productServices.getAllCategory());
        categoryComboBox.setSelectedItem(product.getCategoryName());
        productIdTextField.setText(product.getProductId());
        brandTextField.setText(product.getBrand());
        productNameTextField.setText(product.getProductName());
        colorTextField.setText(product.getColor());
        sizeTextField.setText(product.getSize());
        amountSpinner.setValue(product.getQuantityAvailable());
        unitPriceTextField.setText(String.valueOf(product.getUnitPrice()));
    }
    
    public void setCategoryComboBoxData(List<Category> categories) {
        for (Category c : categories) {
            categoryComboBox.addItem(c.toString());
        }
    }

